    private void btn_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EditarActionPerformed
        Enunciado enunciado = new DTO.Enunciado();
        enunciado.setId(Integer.parseInt((String) cb_Enunciados.getSelectedItem().toString()));
        enunciadoLn.cargarEnunciado(enunciado);
        ExamenesCtrl ex = new ExamenesCtrl();
        ex.abrirEnunciado(enunciado, 1);
        this.hide();
    }//GEN-LAST:event_btn_EditarActionPerformed

    private void btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_EliminarActionPerformed
        Object item = cb_Enunciados.getSelectedItem();
        int enunciadoAeliminar = Integer.parseInt(item.toString());
        int confirmacion = JOptionPane.showConfirmDialog(null, "Se eliminara el enunciado con id " + enunciadoAeliminar + " Â¿Desea realmente eliminar este enuciado?", "Advertencia", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            EnunciadoLN enunciadoLn = new EnunciadoLN();
            enunciadoLn.eliminarEnunciado(enunciadoAeliminar);
            cb_Enunciados.removeItem(item);
            JOptionPane.showMessageDialog(this, "El enunciado con id " + enunciadoAeliminar + " ha sido eliminado exitosamente");
        }
    }//GEN-LAST:event_btn_EliminarActionPerformed

