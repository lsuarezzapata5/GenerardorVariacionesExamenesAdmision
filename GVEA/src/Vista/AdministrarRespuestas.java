    private void actualizarTabla(Tema tt) {
        if (tt.getPreguntas() != null) {
            DefaultTableModel dft = (DefaultTableModel) tbPregResp.getModel();
            limpiarTabla();
            Iterator it = tt.getPreguntas().iterator();
            while (it.hasNext()) {
                PreguntaTema preguntaTema = (PreguntaTema) it.next();
                int rta = preguntaTema.getRespuestaCorrecta();
                String letra = "";
                if (rta == 1) {
                    letra = "A";
                } else if (rta == 2) {
                    letra = "B";
                } else if (rta == 3) {
                    letra = "C";
                } else if (rta == 4) {
                    letra = "D";
                }

                String[] datos = {Integer.toString(preguntaTema.getNumeroPregunta()), letra};
                dft.addRow(datos);
            }
            tbPregResp.setModel(dft);
        }
    }

    public void limpiarTabla() {
        DefaultTableModel dft = (DefaultTableModel) tbPregResp.getModel();
        while (dft.getRowCount() > 0) {
            dft.removeRow(0);
        }
    }

