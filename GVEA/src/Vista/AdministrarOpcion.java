    public AdministrarOpcion(Opcion opcion1, int sel) {
        initComponents();
        setLocationRelativeTo(null);
        ArchivoLN archivoLn = new ArchivoLN();
        this.opcion = opcion1;
        this.seleccion = sel;
        opciones = new ArrayList();
        preguntaLn.listarOpcionesDePRegunta(opcion1.getPregunta());
        opciones = (ArrayList) preguntaLn.listarOpcionesDePRegunta(opcion1.getPregunta());

        DefaultComboBoxModel listaOpciones = (DefaultComboBoxModel) cb_DespuesDeOpcion.getModel();
        listaOpciones.addElement("Ninguno");
        Iterator j = opciones.iterator();
        while (j.hasNext()) {
            Opcion op = (Opcion) j.next();
            if (opcion1.getId() != op.getId()) {
                listaOpciones.addElement(op.getId());
            }
        }

        cb_DespuesDeOpcion.setModel(listaOpciones);
        if (sel == 1) {
            txt_DescripcionOpcion.setText(opcion1.getDescripcion());
            if (opcion1.getDespuesDe().getId() != 0) {
                cb_DespuesDeOpcion.setSelectedIndex(opcion1.getDespuesDe().getId() - 1);

            } else {
                cb_DespuesDeOpcion.setSelectedItem("Ninguno");
            }

            cb_OrdenOpcion.setSelectedIndex(opcion1.getOrden() - 1);
        }

    }

