    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        Pregunta pregunta = new Pregunta();
        pregunta = opcion.getPregunta();
        if (txt_DescripcionOpcion.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la descripci√≥n");
            txt_DescripcionOpcion.requestFocus();
            return;
        }
        String textoEnunciado = txt_DescripcionOpcion.getText();
        char comillaSimple = 39;
        char comillaDoble = 34;
        String nuevoTexto = textoEnunciado.replace(comillaSimple, comillaDoble);
        txt_DescripcionOpcion.setText(nuevoTexto);

        opcion.setDescripcion(txt_DescripcionOpcion.getText());
        Opcion op2 = new Opcion();
        if (cb_DespuesDeOpcion.getSelectedItem() == "Ninguno") {
            op2.setId(0);
        } else {
            if (cb_OrdenOpcion.getSelectedIndex() != 0) {
                JOptionPane.showMessageDialog(this, "Verifique sus condiciones, no pueden ingresar dos condicionamientos diferentes");
                return;
            }
            op2.setId(Integer.parseInt(cb_DespuesDeOpcion.getSelectedItem().toString()));
        }
        opcion.setDespuesDe(op2);
        opcion.setOrden(cb_OrdenOpcion.getSelectedIndex() + 1);
        LatexLN lt = new LatexLN();
        try {
            if (lt.crearOpcion(opcion) == 1) {
                ExamenesCtrl exa = new ExamenesCtrl();
                if (seleccion == 2) {
                    if (opcionLn.crearOpcion(opcion) == 1) {
                        //Duplicar ls backSlashh!
                        lblInformacion.setText("Opcion " + opcion.getId() + " almacenada correctamente!");
                        exa.abrirPregunta(opcion.getPregunta(), 1);
                        this.hide();
                    }

                } else {
                    if (opcionLn.modificarOpcion(opcion) == 1) {
                        lblInformacion.setText("Opcion " + opcion.getId() + " modificada correctamente!");
                        exa.abrirPregunta(opcion.getPregunta(), 1);
                        this.hide();
                    }
                }
            }
        } catch (IOException | InterruptedException ex) {
            Logger.getLogger(AdministrarEnunciado.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_GuardarActionPerformed

