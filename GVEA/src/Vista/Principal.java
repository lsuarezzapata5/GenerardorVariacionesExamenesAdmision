    public void actualizar() {
        temas = temaLn.listarTemas();
        cbTema.removeAllItems();
        Iterator it = temas.iterator();
        while (it.hasNext()) {
            Tema tt = (Tema) it.next();
            cbTema.addItem(tt.getId());
        }
    }

